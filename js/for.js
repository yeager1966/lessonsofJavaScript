// циклы


let c = 50;

while (c <= 55) {
    console.log(c);
    c++;

}
////////////////////////////////////////////////////////////////////////////////
do {
    console.log(c);
    c++;
} while (c <= 55);

///////////////////////////////////////////////////////////////////////////////
for (let i = 1; i <= 8; i++) {
    console.log(i);
}

///////////////////////////////////////////////////////////////////////////////

let b = 10;
for (let i = 1; i <= 8; i++) {
    console.log("i=" + i); // это количество на сколько раз ты будешь ее увеличивать 
    b++;
    console.log("b=" + b);

}

///////////////////////////////////////////////////////////////////////////////

for (let i = 1; i < 0; i++) { // это бесконечный цикл
    console.log("i=" + i);
}

///////////////////////////////////////////////////////////////////////////////


for (let i = 1; i < 10; i++) { // можно прервать на 6
    if (i == 6) {
        break;
    }
    console.log("i=" + i);
}

///////////////////////////////////////////////////////////////////////////////

for (let i = 1; i < 10; i++) { // можно прервать на 6
    if (i == 6) {
        continue; // 6 пропускает 
    }
    console.log("i=" + i);
}